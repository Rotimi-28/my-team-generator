// This is the parent

class Employee {
    constructor(Fname, id, email) {
        this.fname = this.fname;
        this.id = id;
        this.email =email;
    }

    getname() {
        return this.fname;
    }

    getId() {
        return this.id;
    }

    getEmail() {
        return this.email;
    }

    getRole() {
        return 'Employee';
    }
}